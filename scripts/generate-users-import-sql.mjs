import fs from "node:fs";
import path from "node:path";
import Papa from "papaparse";

const [inputPath, outputPath] = process.argv.slice(2);

if (!inputPath || !outputPath) {
  console.error("Usage: node scripts/generate-users-import-sql.mjs <input.csv> <output.sql>");
  process.exit(1);
}

const OWNER_EMAIL = "thomas.soderberg@gmail.com";
const DEFAULT_ROLE_SQL = [
  "INSERT INTO `roles` (`name`, `description`, `permissions`, `isCustom`) VALUES",
  "  ('huvudadmin', 'Huvudadministratör med full åtkomst', '[\"manage_all\", \"manage_roles\", \"manage_users\", \"manage_news\", \"manage_members\", \"view_members\"]', 0),",
  "  ('nyhetsadmin', 'Administratör för nyhetshantering', '[\"manage_news\"]', 0),",
  "  ('medlemsadmin', 'Administratör för medlemshantering', '[\"manage_members\", \"view_members\"]', 0)",
  "ON DUPLICATE KEY UPDATE",
  "  `description` = VALUES(`description`),",
  "  `permissions` = VALUES(`permissions`),",
  "  `isCustom` = VALUES(`isCustom`);",
].join("\n");

const USER_COLUMNS = [
  "id",
  "openId",
  "name",
  "email",
  "password",
  "loginMethod",
  "role",
  "createdAt",
  "updatedAt",
  "lastSignedIn",
  "phone",
  "personnummer",
  "streetAddress",
  "postalCode",
  "city",
  "membershipStatus",
  "membershipNumber",
  "joinYear",
  "memberType",
  "paymentStatus",
  "paymentYear",
  "showInDirectory",
];

function sqlString(value) {
  return `'${String(value).replace(/\\/g, "\\\\").replace(/'/g, "''")}'`;
}

function sqlValue(value) {
  if (value === undefined || value === null) {
    return "NULL";
  }

  const trimmed = String(value).trim();
  if (!trimmed) {
    return "NULL";
  }

  if (/^-?\d+$/.test(trimmed) && !/^0\d+/.test(trimmed)) {
    return trimmed;
  }

  return sqlString(trimmed);
}

function normalizeRow(rawRow) {
  const row = { ...rawRow };
  const email = (row.email || "").trim().toLowerCase();
  row.email = email || null;

  row.password = row.password || null;
  row.loginMethod = row.email === OWNER_EMAIL ? "password" : row.loginMethod || null;
  row.role = row.role || "user";
  row.showInDirectory = row.showInDirectory || "1";
  row.personnummer = row.personnummer || null;
  row.membershipNumber = row.membershipNumber || null;
  row.paymentYear = row.paymentYear || null;
  row.joinYear = row.joinYear || null;

  return row;
}

const csv = fs.readFileSync(inputPath, "utf8");
const parsed = Papa.parse(csv, { header: true, skipEmptyLines: true });

if (parsed.errors.length) {
  console.error("CSV parse failed:", parsed.errors[0]);
  process.exit(1);
}

const rows = parsed.data.map(normalizeRow);

if (!rows.length) {
  console.error("No rows found in CSV");
  process.exit(1);
}

const ownerRow = rows.find((row) => row.email === OWNER_EMAIL) ?? null;

const statements = rows.map((row) => {
  const values = USER_COLUMNS.map((column) => sqlValue(row[column]));

  const updates = USER_COLUMNS
    .filter((column) => column !== "id")
    .map((column) => `  \`${column}\` = VALUES(\`${column}\`)`)
    .join(",\n");

  return [
    `INSERT INTO \`users\` (${USER_COLUMNS.map((column) => `\`${column}\``).join(", ")})`,
    `VALUES (${values.join(", " )})`,
    "ON DUPLICATE KEY UPDATE",
    `${updates};`,
  ].join("\n");
});

const sql = [
  "-- Generated by scripts/generate-users-import-sql.mjs",
  `-- Source CSV: ${inputPath}`,
  `-- Rows: ${rows.length}`,
  "-- Imports Manus users into Hostinger MySQL with schema compatibility guards.",
  "",
  "START TRANSACTION;",
  "",
  "CREATE TABLE IF NOT EXISTS `users` (",
  "  `id` int NOT NULL AUTO_INCREMENT,",
  "  `openId` varchar(64) NOT NULL,",
  "  `name` text NULL,",
  "  `email` varchar(320) NULL,",
  "  `password` varchar(255) NULL,",
  "  `loginMethod` varchar(64) NULL,",
  "  `role` enum('user','admin','member') NOT NULL DEFAULT 'user',",
  "  `roleId` int NULL,",
  "  `phone` varchar(20) NULL,",
  "  `personnummer` varchar(13) NULL,",
  "  `streetAddress` text NULL,",
  "  `postalCode` varchar(10) NULL,",
  "  `city` varchar(100) NULL,",
  "  `membershipStatus` enum('pending','active','inactive') NULL DEFAULT 'pending',",
  "  `membershipNumber` varchar(50) NULL,",
  "  `joinYear` int NULL,",
  "  `memberType` enum('ordinarie','hedersmedlem','stodmedlem') NULL DEFAULT 'ordinarie',",
  "  `paymentStatus` enum('paid','unpaid','exempt') NULL DEFAULT 'unpaid',",
  "  `paymentYear` int NULL,",
  "  `showInDirectory` int NOT NULL DEFAULT 1,",
  "  `createdAt` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,",
  "  `updatedAt` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,",
  "  `lastSignedIn` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,",
  "  PRIMARY KEY (`id`),",
  "  UNIQUE KEY `users_openId_unique` (`openId`),",
  "  UNIQUE KEY `users_personnummer_unique` (`personnummer`),",
  "  UNIQUE KEY `users_membershipNumber_unique` (`membershipNumber`)",
  ");",
  "",
  "CREATE TABLE IF NOT EXISTS `roles` (",
  "  `id` int AUTO_INCREMENT NOT NULL,",
  "  `name` varchar(50) NOT NULL,",
  "  `description` text,",
  "  `permissions` json NOT NULL,",
  "  `isCustom` int NOT NULL DEFAULT 1,",
  "  `createdAt` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,",
  "  `updatedAt` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,",
  "  CONSTRAINT `roles_id` PRIMARY KEY(`id`),",
  "  CONSTRAINT `roles_name_unique` UNIQUE(`name`)",
  ");",
  "",
  "ALTER TABLE `users` ADD COLUMN IF NOT EXISTS `password` varchar(255);",
  "ALTER TABLE `users` ADD COLUMN IF NOT EXISTS `roleId` int;",
  "ALTER TABLE `users` ADD COLUMN IF NOT EXISTS `personnummer` varchar(13);",
  "ALTER TABLE `users` ADD COLUMN IF NOT EXISTS `streetAddress` text;",
  "ALTER TABLE `users` ADD COLUMN IF NOT EXISTS `postalCode` varchar(10);",
  "ALTER TABLE `users` ADD COLUMN IF NOT EXISTS `city` varchar(100);",
  "ALTER TABLE `users` ADD COLUMN IF NOT EXISTS `joinYear` int;",
  "ALTER TABLE `users` ADD COLUMN IF NOT EXISTS `memberType` enum('ordinarie','hedersmedlem','stodmedlem') DEFAULT 'ordinarie';",
  "ALTER TABLE `users` ADD COLUMN IF NOT EXISTS `paymentStatus` enum('paid','unpaid','exempt') DEFAULT 'unpaid';",
  "ALTER TABLE `users` ADD COLUMN IF NOT EXISTS `paymentYear` int;",
  "ALTER TABLE `users` ADD COLUMN IF NOT EXISTS `showInDirectory` int NOT NULL DEFAULT 1;",
  "",
  DEFAULT_ROLE_SQL,
  "",
  ownerRow
    ? `DELETE FROM \`users\` WHERE \`email\` = ${sqlString(OWNER_EMAIL)} AND \`id\` <> ${sqlValue(ownerRow.id)};`
    : `-- No imported row matched ${OWNER_EMAIL}; skipping owner email cleanup.`,
  "",
  ...statements,
  "",
  "UPDATE `users`",
  "SET `roleId` = (SELECT `id` FROM `roles` WHERE `name` = 'huvudadmin' LIMIT 1)",
  `WHERE \`email\` = ${sqlString(OWNER_EMAIL)} OR \`role\` = 'admin';`,
  "",
  "COMMIT;",
  "",
].join("\n");

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, sql, "utf8");

console.log(`Wrote ${rows.length} users to ${outputPath}`);
